create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  full_name text,
  created_at timestamptz not null default now()
);

create table if not exists public.ea_instances (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  name text not null,
  symbol text default 'XAUUSD',
  account_login text,
  token_hash text not null,
  enabled boolean not null default true,
  last_seen_at timestamptz,
  created_at timestamptz not null default now()
);

create table if not exists public.ea_states (
  ea_id uuid primary key references public.ea_instances(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  state jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now()
);

create table if not exists public.commands (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  ea_id uuid not null references public.ea_instances(id) on delete cascade,
  client_id text,
  action text not null,
  payload jsonb not null default '{}'::jsonb,
  status text not null default 'pending' check (status in ('pending','sent','done','failed','expired')),
  result_message text,
  created_at timestamptz not null default now(),
  sent_at timestamptz,
  done_at timestamptz,
  expires_at timestamptz not null default (now() + interval '45 seconds')
);

create table if not exists public.audit_logs (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete set null,
  ea_id uuid references public.ea_instances(id) on delete set null,
  action text not null,
  details jsonb default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.telegram_settings (
  user_id uuid primary key references auth.users(id) on delete cascade,
  bot_token_cipher text,
  chat_id text,
  bot_username text,
  enabled boolean not null default false,
  prefs jsonb not null default '{
    "trade_open": true,
    "sl_hit": true,
    "tp_hit": true,
    "rr1_hit": true,
    "rr2_hit": true,
    "rr3_hit": true,
    "partial_hit": true,
    "command_sent": true,
    "command_done": true,
    "command_failed": true,
    "ea_message": true,
    "ea_online": true,
    "ea_offline": true
  }'::jsonb,
  connected_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.notification_events (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  ea_id uuid not null references public.ea_instances(id) on delete cascade,
  event_type text not null,
  event_key text not null,
  payload jsonb not null default '{}'::jsonb,
  status text not null default 'pending' check (status in ('pending','sent','skipped','failed')),
  error text,
  telegram_message_id text,
  created_at timestamptz not null default now(),
  sent_at timestamptz,
  unique (user_id, ea_id, event_type, event_key)
);

create table if not exists public.telegram_ea_status (
  ea_id uuid primary key references public.ea_instances(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  is_online boolean not null default false,
  last_online_at timestamptz,
  last_offline_at timestamptz,
  updated_at timestamptz not null default now()
);

create index if not exists idx_ea_instances_user_id on public.ea_instances(user_id);
create index if not exists idx_commands_ea_pending on public.commands(ea_id, status, created_at);
create index if not exists idx_commands_pending_fast on public.commands(ea_id, created_at) where status = 'pending';
create index if not exists idx_commands_pending_expiry on public.commands(ea_id, expires_at) where status = 'pending';
create unique index if not exists idx_commands_ea_client_id_unique on public.commands(ea_id, client_id) where client_id is not null;
create index if not exists idx_commands_user_created on public.commands(user_id, created_at desc);
create index if not exists idx_notification_events_user_created on public.notification_events(user_id, created_at desc);
create index if not exists idx_telegram_ea_status_user_id on public.telegram_ea_status(user_id);

