create or replace function public.tcx_telegram_merge_prefs(p_prefs jsonb)
returns jsonb as $$
declare
  v_out jsonb := public.tcx_telegram_default_prefs();
  v_details jsonb := public.tcx_telegram_default_detail_prefs();
  v_key text;
