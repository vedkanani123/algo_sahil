  for v_line in select regexp_split_to_table(coalesce(p_text, ''), E'\n')
  loop
    v_key := public.tcx_telegram_line_detail_key(v_line);
    if v_key is not null and coalesce((v_details ->> v_key)::boolean, true) = false then
